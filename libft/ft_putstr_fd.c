#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	int	i;

	i = 0;
	if (!s)
		ft_putstr_fd("(null)", fd);
	while (s && s[i])
	{
		ft_putchar_fd(s[i], fd);
		i++;
	}
}
